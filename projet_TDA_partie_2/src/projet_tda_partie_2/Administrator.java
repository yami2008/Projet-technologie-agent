    public Administrator() throws FileNotFoundException, IOException {
        initComponents();
        File folder = new File("laptops/");
        File[] listOfFiles = folder.listFiles();
        for (File file : listOfFiles) {
            if (file.isFile()) {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel() ;
                model.addRow(new Object[]{
                    file.getName().substring(0,(file.getName().length()-4))
                });
                BufferedReader br = new BufferedReader(new FileReader(file));
                if (br != null){
                    String line = br.readLine();
                    if (line != null){
                        String[] name = line.split(",");
                        model = (DefaultTableModel) jTable2.getModel() ;
                        model.setColumnIdentifiers(name);
                        Object[] tabLine = br.lines().toArray();
                        for (int i = 0 ; i < tabLine.length ; i++){
                            line = tabLine[i].toString().trim();
                            String[] dataRow = line.split(",");
                            model.addRow(dataRow);
                        }
                    }
                }
                
            }
        }
    }

   
